    private void btnSaveVitalSignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveVitalSignActionPerformed
        // TODO add your handling code here:
        
        
        
        try {
            String respiratoryRate = txtRespiratoryRate.getText().toString();
            String heartRate = txtHeartRate.getText().toString();
            String bloodPressure = txtBloodPressure.getText().toString();
            String weight = txtWeight.getText().toString();

            VitalSigns vitalSign = person.getVitalSign().addVitalSign();
            vitalSign.setRespiratoryRate(Integer.parseInt(respiratoryRate));
            vitalSign.setHeartRate(Integer.parseInt(heartRate));
            vitalSign.setBloodPressure(Integer.parseInt(bloodPressure));
            vitalSign.setWeight(Integer.parseInt(weight));
            /*Set current date and time as TimeStamp*/
            vitalSign.setTimestamp(new Date());

            JOptionPane.showMessageDialog(workAreaJPanel, vitalSign);
            JOptionPane.showMessageDialog(this, "Vital signs added!!", "Update",
                JOptionPane.INFORMATION_MESSAGE);
            clearFields();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Please enter correct values",
                "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSaveVitalSignActionPerformed

