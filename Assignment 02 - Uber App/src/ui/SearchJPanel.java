    private void btnSearchCarManufacturerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchCarManufacturerActionPerformed
        // TODO add your handling code here:
        String carManufacturer = txtSearchCarManufacturer.getText();
        
        DefaultTableModel model = (DefaultTableModel) tblSearchCarDetails.getModel();
        
        model.setRowCount(0);
        int count =0;
        for(CarInfo carInfo : history.getHistory()){
            
            if(carInfo.getCarManufacturer().equals(carManufacturer)){
                count++;
                
                JOptionPane.showMessageDialog(this, "Record Found!!");
                
                Object[] row = new Object[9];
                
                row[0]= carInfo;
                row[1]= carInfo.getCarModelname();
                row[2]= carInfo.getCarModelNum();
                row[3]= carInfo.getYearOfManufacturing();
                row[4]= carInfo.getSerialNumber();
                row[5]= carInfo.getCity();
                row[6]= carInfo.getCountOfSeats();
                row[7]= carInfo.getAvailability();
                row[8]= carInfo.getMaintainenceCertStatus();
             
                
                model.addRow(row);
                
            
            }else{
                
                JOptionPane.showMessageDialog(this, "Record not Found!!");
            
            }
        }
      
    }//GEN-LAST:event_btnSearchCarManufacturerActionPerformed

